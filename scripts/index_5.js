/*
Grades (compared to benchmarkValue)
5: A (40%) 
4: B (10% | 40%) 30% total
3: C (-10% | 10%) 20% total
2: D (-10% | -40%) 30% total
1: F (-40%)
*/

function grading(value, bAverage, bType) {

	// check benchmark value vs actual value && benchmark type
	if (value < bAverage - (bAverage * 0.9) && bType == 0) {
		return "A";
	}
	else if (value > bAverage + (bAverage * 0.9) && bType == 1) {
		return "A";
	}

	else if (value > bAverage + (bAverage * 0.9) && bType == 0) {
		return "F";
	}
	else if (value < bAverage - (bAverage * 0.9) && bType == 1) {
		return "F";
	}

	else if (value < bAverage - (bAverage * 0.3) && bType == 0) {
		return "B";
	}
	else if (value > bAverage + (bAverage * 0.3) && bType == 1) {
		return "B";
	}

	else if (value > bAverage + (bAverage * 0.3) && bType == 0) {
		return "D"
	}
	else if (value < bAverage - (bAverage * 0.3) && bType == 1) {
		return "D"
	}
	else {
		return "C";
	}
}

function convertGrade(grade) {
	if (grade == "A") {
		return 5;
	}
	else if (grade == "B") {
		return 4;
	}
	else if (grade == "C") {
		return 3;
	}
	else if (grade == "D") {
		return 2;
	}
	else if (grade == "F") {
		return 1;
	}
}

function finalGrade(holisticGrade) {
	if (holisticGrade > 4.8) {
		return "A";
	}
	else if (holisticGrade > 4.5) {
		return "A-";
	}

	else if (holisticGrade > 4.2) {
		return "B+";
	}
	else if (holisticGrade > 3.8) {
		return "B";
	}
	else if (holisticGrade > 4.5) {
		return "B-";
	}

	else if (holisticGrade > 4.2) {
		return "C+";
	}
	else if (holisticGrade > 2.8) {
		return "C";
	}
	else if (holisticGrade > 2.5) {
		return "C-";
	}

	else if (holisticGrade > 2.2) {
		return "D+";
	}
	else if (holisticGrade > 1.8) {
		return "D";
	}
	else if (holisticGrade > 1.5) {
		return "D-";
	}

	else {
		return "F";
	}
}

